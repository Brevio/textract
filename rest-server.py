@app.route('/textract-gcqi/ocr', methods=['POST'])
def extract_text():
    data_json = request.json
    imagebase64 = data_json.get("fileBase64")
    fileName = data_json.get("fileName")
    ocr = Ocr()
    file_in = ocr.decode(imagebase64,fileName)
    return ocr.extract(file_in)

